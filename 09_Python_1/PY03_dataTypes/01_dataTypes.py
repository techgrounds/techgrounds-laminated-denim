#The single quotation mark signifies an "Identifier", a name used to identify a variable, function, class, module or object.
a = 'int'
#The variable below refers to an integer, a whole number.
b = 7
#c is a boolean variable, True or False.
c = False
#d is a string, with a decimal number that Python identifies as atext.
d = "18.5"
#The type function identifies the data type of the variable.
print(type(a))
print(type(b))
print(type(c))
print(type(d))
#float looks for a decimal number in a string and converts it to a floating point number.
x = b + float(d)
print(x)
